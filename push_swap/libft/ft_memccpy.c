#include <string.h>

void	*ft_memccpy(void *s1, const void *s2, int c, size_t n)
{
	unsigned char	*ss1;
	unsigned char	*ss2;

	ss1 = (unsigned char *)s1;
	ss2 = (unsigned char *)s2;
	while (n--)
	{
		*(ss1++) = *ss2;
		if ((unsigned char)c == *ss2)
			return (ss1);
		ss2++;
	}
	return (NULL);
}
